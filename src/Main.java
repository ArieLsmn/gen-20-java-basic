
public class Main { // deklarasi class java, biasanya nama Main digunakan untuk class yang menjalankan fungsi utama program

    
    
    public static void main(String[] args) { // deklarasi fungsi custom, yang disebut sebagai method, khusus untuk fungsi "public static void main" digunakan sebagai fungsi utama untuk me-run sebuah project java

        System.out.print("Hello!");     // fungsi System.out.print digunakan untuk melakukan print suatu teks ke console
        
        System.out.println("Hello world!"); // fungsi System.out.println digunakan untuk melakukan print suatu teks ke console disertai newline
    }
}
